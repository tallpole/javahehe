package basics;

public class Days {
	public static void main(String[] args) {
		//Execute different blocks of code based on the value of condition
		String dayOfWeek = "Tuesday";
		switch (dayOfWeek) {
		case "Monday" : 
			System.out.println("Today is " + dayOfWeek);
			break;
		case "Tuesday" : 
			System.out.println("Today is " + dayOfWeek);
			break;
		case "Wednesday" : 
			System.out.println("Today is " + dayOfWeek);
			break;
		case "Thursday" : 
			System.out.println("Today is " + dayOfWeek);
			break;
		case "Friday" : 
			System.out.println("Today is " + dayOfWeek);
			break;
		case "Saturday" : 
			System.out.println("Today is " + dayOfWeek);
			break;
		case "Sunday" : 
			System.out.println("Today is " + dayOfWeek);
			break;
		}
		
	}

}
